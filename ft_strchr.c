#include "libft.h"

/*
**	Возвращает указатель на местонахождение первого совпадения с символом
**	c в строке s.
**
**	Eсли символ не найден, то возвращают NULL
*/

char	*ft_strchr(const char *s, int c)
{
	unsigned char	ch;

	ch = (unsigned char)c;
	while (*s != c)
	{
		if (!*s)
			return (NULL);
		s++;
	}
	return ((char*)s);
}
